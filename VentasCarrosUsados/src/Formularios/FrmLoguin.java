    private boolean usuarioAutorizado(Usuario _usuario) {
        ResultSet rs=null;                       
        PreparedStatement st=null;
        try
        {   
            con = Conexion.Conexion.conectar();
            st = con.prepareStatement("SELECT * FROM usuarios WHERE usuario = ? and clave = ? and estado = ?");            
            st.setString(1,_usuario.getUsuario()); 
            st.setString(2, _usuario.getClave());
            st.setString(3, "activo");
            rs = st.executeQuery(); 
            if(rs.next()){
                _usuario.setRol(rs.getString("rol"));                
                return true;
            }   
            return false;
        }        
        catch(Exception e){
            System.out.println("Error en la consulta de usuario. \n"+e);            
            return false;
        } finally{
            if ( con!=null) {
                try {
                    con.close();
                } catch (SQLException ex) {
                    //Logger.getLogger(FrmIngresarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (st!=null) {
                try{
                    st.close();
                }catch (SQLException ex) {
                   // Logger.getLogger(FrmIngresarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (rs!= null) {
                try{
                    rs.close();
                }catch (SQLException ex) {
                    //Logger.getLogger(FrmIngresarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }
